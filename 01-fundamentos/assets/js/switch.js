


const dia = 2; // 0=dom, 1=lunes...etc

switch (dia) {
    case 0:
        console.log('domingo');
        break;
    case 1:
        console.log('Lunes');
        break;
    case 2:
        console.log('Martes');
        break;
    default:
        console.log('no es dom, lunes ni martes');
        break;
}

